# Conditional Expression (Ternary Operator) in Python : A one-liner alternative to if-else statements
#                                                       Print or assign one of two values based on a condition
#                                                       X if condition else Y

num = 5

print("Even" if num % 2 == 0 else "Odd")  # Output: Odd
print("Positive" if num > 0 else "Negative")  # Output: Positive